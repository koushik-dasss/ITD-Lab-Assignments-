SELECT NAME
FROM INSTRUCTOR
WHERE DEPT_NAME = (
    SELECT DEPT_NAME
    FROM COURSE
    WHERE COURSE_ID = 'CS-101'
);

SELECT DEPT_NAME
FROM DEPARTMENT
WHERE BUDGET > (
    SELECT BUDGET
    FROM DEPARTMENT
    WHERE DEPT_NAME = 'Biology'
);

SELECT DISTINCT S.NAME
FROM STUDENT S
JOIN TAKES T ON S.ID = T.S_ID
JOIN SECTION SEC ON T.COURSE_ID = SEC.COURSE_ID
WHERE SEC.ROOM_NO = (
    SELECT ROOM_NO
    FROM SECTION
    WHERE COURSE_ID = 'CS-101'
    LIMIT 1
);

SELECT S.ID, S.NAME
FROM STUDENT S
WHERE NOT EXISTS (
    (SELECT COURSE_ID
     FROM COURSE
     WHERE DEPT_NAME = 'Comp. Sci.')
    EXCEPT
    (SELECT COURSE_ID
     FROM TAKES
     WHERE S_ID = S.ID)
);

SELECT DISTINCT COURSE_ID
FROM SECTION
WHERE BUILDING = (
    SELECT BUILDING
    FROM CLASSROOM
    WHERE ROOM_NO = 514
);

SELECT NAME
FROM INSTRUCTOR I
