SELECT DISTINCT CL.BUILDING, CL.CAPACITY
FROM CLASSROOM CL
JOIN SECTION SEC ON CL.ROOM_NO = SEC.ROOM_NO AND CL.BUILDING = SEC.BUILDING
JOIN COURSE C ON SEC.COURSE_ID = C.COURSE_ID
WHERE C.DEPT_NAME = 'Comp. Sci.';

